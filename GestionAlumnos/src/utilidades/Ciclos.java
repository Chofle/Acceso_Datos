package utilidades;

import java.util.List;

public class Ciclos {

	private List<Ciclo> ciclos;

	public List<Ciclo> getCiclos() {
		return ciclos;
	}

	public void setCiclos(List<Ciclo> ciclos) {
		this.ciclos = ciclos;
	}

	@Override
	public String toString() {
		return "Ciclo [ciclos=" + ciclos + "]";
	}

}
